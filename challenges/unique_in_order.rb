def unique_in_order(seq)

	new_arr = []

	seq = seq.chars if seq == seq.to_s

	index = 0
	next_index = 1 

	loop do
		break if seq.empty?

		if seq[index] == seq[next_index]
			seq.delete_at(next_index)
		else
			new_arr << seq.shift
		end

	end

	new_arr

end

